bool ПроверкаНаПростоеЧисло(int число)
{
    if (число <= 1)
    {
        return false; // 1 и отрицательные числа не являются простыми
    }
    else if (число <= 3)
    {
        return true; // 2 и 3 являются простыми
    }
    else if (число % 2 == 0 || число % 3 == 0)
    {
        return false; // числа, делящиеся на 2 или 3, не являются простыми
    }
    else
    {
        int i = 5;
        while (i * i <= число)
        {
            if (число % i == 0 || число % (i + 2) == 0)
            {
                return false; // если число делится на i или i+2, то оно не является простым
            }
            i += 6;
        }
        return true; // если число не делится ни на одно из предыдущих чисел, то оно простое
    }
}
